    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
          conneccion();
        try {
            connection = con.getConnection();
            preparedStatement = connection.prepareStatement("INSERT INTO Grados (idGrados,Descripcion) VALUES (?,?)");
            preparedStatement.setInt(1, Integer.parseInt (txtId.getText()));
            preparedStatement.setString(2, txtDescripcion.getText());
            int res = preparedStatement.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Personal Guardado");
                Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al Guardar Personal");
                Limpiar();
            }

            connection.close();

        } catch (Exception ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

