    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
          conneccion();
        try {
            connection = con.getConnection();
            preparedStatement = connection.prepareStatement("SELECT idGrados, Descripcion FROM Grados WHERE idGrados=?");
            preparedStatement.setInt(1, Integer.parseInt (txtId.getText()));
            resultSet = preparedStatement.executeQuery();
            if (resultSet.next()) {
            preparedStatement = connection.prepareStatement("INSERT INTO Grados (idGrados,Descripcion) VALUES (?,?,?,?,?,?)");
            txtId.setText(resultSet.getString("idGrados"));
            txtDescripcion.setText(resultSet.getString("Descripcion"));

            } else {
                JOptionPane.showMessageDialog(null, "No existe un personal con esta clave");
                txtId.setEditable(true);
            }

            connection.close();

        } catch (Exception ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

